  $( "#save1" ).click(function() {
    var id = $(this).data("attribute");
    var idString = "#" + id;
    var idValue = $(idString);
    var saveValue = idValue.val();
    var time = $(this).parent().attr("id");
    console.log(time);
    localStorage.setItem("6AM",saveValue);
});

